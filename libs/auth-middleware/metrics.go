package authmw

import (
	"sync"

	"github.com/prometheus/client_golang/prometheus"
)

// Counters live as package-level singletons so the increment helpers
// can be called from anywhere in the package without the caller having
// to thread a Metrics struct through. Mirrors the singleton pattern in
// libs/ontology-kernel/metrics.
var (
	metricsOnce sync.Once

	invalidTokenCounter  *prometheus.CounterVec
	secretAutogenCounter *prometheus.CounterVec
)

func initMetrics() {
	metricsOnce.Do(func() {
		invalidTokenCounter = prometheus.NewCounterVec(
			prometheus.CounterOpts{
				Name: "auth_invalid_token_total",
				Help: "JWTs that failed validation, partitioned by failure reason.",
			},
			[]string{"reason"},
		)
		secretAutogenCounter = prometheus.NewCounterVec(
			prometheus.CounterOpts{
				Name: "auth_secret_autogenerated_total",
				Help: "Number of times the HS256 JWT secret was auto-generated on first boot at the given path. Non-zero in production is a critical incident — see libs/auth-middleware/doc.go.",
			},
			[]string{"path"},
		)
	})
}

// RegisterMetrics registers the auth-middleware Prometheus counters
// against the supplied registry. Safe to call from multiple binaries;
// re-registering the same collector returns AlreadyRegisteredError,
// which is silently ignored (mirrors the convention used by
// libs/ontology-kernel/metrics.RegisterActionMetrics).
//
// Counters are usable (incrementable) even when this function is
// never called — they just won't be exported on /metrics. Tests can
// observe values via prometheus/testutil.ToFloat64 regardless.
func RegisterMetrics(registry prometheus.Registerer) {
	initMetrics()
	_ = registry.Register(invalidTokenCounter)
	_ = registry.Register(secretAutogenCounter)
}

func recordInvalidToken(reason string) {
	initMetrics()
	invalidTokenCounter.WithLabelValues(reason).Inc()
}

func recordSecretAutogenerated(path string) {
	initMetrics()
	secretAutogenCounter.WithLabelValues(path).Inc()
}

// InvalidTokenCounter exposes the underlying CounterVec for tests that
// need to assert on label values. Production code should call
// recordInvalidToken via the middleware path.
func InvalidTokenCounter() *prometheus.CounterVec {
	initMetrics()
	return invalidTokenCounter
}

// SecretAutogenCounter exposes the underlying CounterVec for tests.
func SecretAutogenCounter() *prometheus.CounterVec {
	initMetrics()
	return secretAutogenCounter
}
