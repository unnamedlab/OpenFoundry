fn property_description(field: &crate::models::hyperauto::HyperAutoErpFieldPlan) -> String {
    match field.semantic_role.as_str() {
        "primary_key" => {
            format!(
                "Primary key field derived from ERP source column '{}'.",
                field.source_name
            )
        }
        "reference_candidate" => format!(
            "ERP foreign-key candidate derived from source column '{}'.",
            field.source_name
        ),
        _ => format!(
            "Auto-generated ERP attribute derived from source column '{}'.",
            field.source_name
        ),
    }
}

fn display_name_for_property(raw: &str) -> String {
    raw.split('_')
        .filter(|part| !part.is_empty())
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                Some(first) => {
                    let rest = chars.as_str().to_ascii_lowercase();
                    format!("{}{}", first.to_ascii_uppercase(), rest)
                }
                None => String::new(),
            }
        })
        .collect::<Vec<_>>()
        .join(" ")
}

fn slugify(raw: &str) -> String {
    let mut slug = String::new();
    let mut separator = true;
    for ch in raw.chars() {
        if ch.is_ascii_alphanumeric() {
            slug.push(ch.to_ascii_lowercase());
            separator = false;
        } else if !separator {
            slug.push('_');
            separator = true;
        }
    }
    slug.trim_matches('_').to_string()
}

async fn load_connection(
    state: &AppState,
    connection_id: Uuid,
) -> Result<Connection, axum::response::Response> {
    match sqlx::query_as::<_, Connection>("SELECT * FROM connections WHERE id = $1")
        .bind(connection_id)
        .fetch_optional(&state.db)
        .await
    {
        Ok(Some(connection)) => Ok(connection),
        Ok(None) => Err(StatusCode::NOT_FOUND.into_response()),
        Err(error) => {
            tracing::error!("connection lookup failed: {error}");
            Err(StatusCode::INTERNAL_SERVER_ERROR.into_response())
        }
    }
}
