    pub async fn create_foundry_branch(
        &self,
        branch: NewFoundryBranch<'_>,
    ) -> Result<RuntimeBranch, sqlx::Error> {
        let mut tx = self.pool.begin().await?;
        let returning_cols = BRANCH_COLUMNS;
        let sql = format!(
            r#"INSERT INTO dataset_branches (
                   id, dataset_id, dataset_rid, name,
                   parent_branch_id, head_transaction_id, created_from_transaction_id,
                   version, base_version, description, is_default, created_by,
                   labels, fallback_chain
               )
               VALUES ($1, $2, $3, $4, $5, $6, $7,
                       COALESCE((SELECT version FROM dataset_branches WHERE id = $5), 1),
                       1, $8, FALSE, $9,
                       $10::jsonb, $11)
               RETURNING {cols}"#,
            cols = returning_cols,
        );
        let row: RuntimeBranch = sqlx::query_as(&sql)
            .bind(branch.id)
            .bind(branch.dataset_id)
            .bind(branch.dataset_rid)
            .bind(branch.name)
            .bind(branch.parent_branch_id)
            .bind(branch.head_transaction_id)
            .bind(branch.created_from_transaction_id)
            .bind(branch.description)
            .bind(branch.created_by)
            .bind(&branch.labels)
            .bind(&branch.fallback_chain)
            .fetch_one(&mut *tx)
            .await?;

        // Mirror the array column into `dataset_branch_fallbacks` so
        // existing readers (foundry list_fallbacks, fallback resolver)
        // see the chain immediately.
        if !branch.fallback_chain.is_empty() {
            for (position, name) in branch.fallback_chain.iter().enumerate() {
                let trimmed = name.trim();
                if trimmed.is_empty() {
                    continue;
                }
                sqlx::query(
                    r#"INSERT INTO dataset_branch_fallbacks
                           (branch_id, position, fallback_branch_name)
                       VALUES ($1, $2, $3)
                       ON CONFLICT (branch_id, position) DO NOTHING"#,
                )
                .bind(row.id)
                .bind(position as i32)
                .bind(trimmed)
                .execute(&mut *tx)
                .await?;
            }
        }

        tx.commit().await?;
        Ok(row)
    }

    pub async fn soft_delete_branch(
        &self,
        branch_id: Uuid,
        new_parent_id: Option<Uuid>,
    ) -> Result<(), sqlx::Error> {
        let mut tx = self.pool.begin().await?;

        sqlx::query(
            r#"UPDATE dataset_branches
                  SET parent_branch_id = $2
                WHERE parent_branch_id = $1 AND deleted_at IS NULL"#,
        )
        .bind(branch_id)
        .bind(new_parent_id)
        .execute(&mut *tx)
        .await?;

        sqlx::query(
            r#"UPDATE dataset_branches
                  SET deleted_at = NOW(), updated_at = NOW()
                WHERE id = $1 AND deleted_at IS NULL"#,
        )
        .bind(branch_id)
        .execute(&mut *tx)
        .await?;

        tx.commit().await
    }

