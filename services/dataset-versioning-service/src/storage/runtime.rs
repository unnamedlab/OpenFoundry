//! Runtime store boundary for dataset versioning state.
//!
//! Ownership is intentionally split:
//! * `datasets` stays in Postgres as declarative metadata for the dataset
//!   catalog surface (identity, active branch pointer, counters).
//! * `dataset_branches`, `dataset_versions`, `dataset_transactions` and
//!   their derived runtime tables live behind this module. Callers should
//!   treat this as the single Postgres-backed coordination/runtime store.

use chrono::{DateTime, Utc};
use serde::Serialize;
use serde_json::Value;
use sqlx::{PgPool, Postgres, Transaction};
use uuid::Uuid;

use crate::models::{
    branch::DatasetBranch, dataset::Dataset, transaction::DatasetTransaction,
    version::DatasetVersion,
};

#[derive(Debug, Clone, Serialize, sqlx::FromRow)]
pub struct RuntimeBranch {
    pub id: Uuid,
    /// `ri.foundry.main.branch.<uuid>`. Stored-generated by Postgres
    /// (migration `20260504000010_branches_unify`).
    #[serde(default)]
    pub rid: String,
    pub dataset_id: Uuid,
    /// `ri.foundry.main.dataset.<uuid>`.
    #[serde(default)]
    pub dataset_rid: String,
    pub name: String,
    pub parent_branch_id: Option<Uuid>,
    pub head_transaction_id: Option<Uuid>,
    /// Transaction the branch was forked off via
    /// `source.from_transaction_rid`. NULL when forked off another
    /// branch's HEAD or as a root.
    #[serde(default)]
    pub created_from_transaction_id: Option<Uuid>,
    /// Bumped by trigger on every transaction insert for this branch.
    pub last_activity_at: DateTime<Utc>,
    #[serde(default)]
    pub labels: serde_json::Value,
    /// Denormalised cache of `dataset_branch_fallbacks` (kept in sync
    /// by [`RuntimeStore::replace_fallbacks`]).
    #[serde(default)]
    pub fallback_chain: Vec<String>,
    pub created_at: DateTime<Utc>,
    pub updated_at: DateTime<Utc>,
}

/// Columns we always read for [`RuntimeBranch`] — kept in one place so
/// every loader returns a fully-populated row.
const BRANCH_COLUMNS: &str = r#"id, rid, dataset_id, dataset_rid, name,
        parent_branch_id, head_transaction_id, created_from_transaction_id,
        last_activity_at, labels, fallback_chain, created_at, updated_at"#;

