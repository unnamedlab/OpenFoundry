    pub async fn ensure_default_branch(&self, dataset: &Dataset) -> Result<(), sqlx::Error> {
        if !self.any_branch_exists(dataset.id).await? {
            sqlx::query(
                r#"INSERT INTO dataset_branches (
                       id, dataset_id, name, version, base_version, description, is_default
                   )
                   VALUES ($1, $2, 'main', $3, $3, 'Default branch', TRUE)"#,
            )
            .bind(Uuid::now_v7())
            .bind(dataset.id)
            .bind(dataset.current_version)
            .execute(&self.pool)
            .await?;
        }

        Ok(())
    }

