    pub async fn upsert_view(
        &self,
        dataset_id: Uuid,
        branch_id: Uuid,
        head_transaction_id: Uuid,
        file_count: i32,
        size_bytes: i64,
    ) -> Result<Uuid, sqlx::Error> {
        sqlx::query_scalar::<_, Uuid>(
            r#"INSERT INTO dataset_views (
                   id, dataset_id, branch_id, head_transaction_id,
                   computed_at, file_count, size_bytes
               )
               VALUES ($1, $2, $3, $4, NOW(), $5, $6)
               ON CONFLICT (dataset_id, branch_id, head_transaction_id) DO UPDATE
                  SET computed_at = NOW()
               RETURNING id"#,
        )
        .bind(Uuid::now_v7())
        .bind(dataset_id)
        .bind(branch_id)
        .bind(head_transaction_id)
        .bind(file_count)
        .bind(size_bytes)
        .fetch_one(&self.pool)
        .await
    }

    pub async fn replace_view_files(
        &self,
        view_id: Uuid,
        files: &[RuntimeViewFile],
    ) -> Result<(), sqlx::Error> {
        let mut tx = self.pool.begin().await?;

        sqlx::query("DELETE FROM dataset_view_files WHERE view_id = $1")
            .bind(view_id)
            .execute(&mut *tx)
            .await?;

        for file in files {
            sqlx::query(
                r#"INSERT INTO dataset_view_files
                       (view_id, logical_path, physical_path, size_bytes, introduced_by)
                   VALUES ($1, $2, $3, $4, $5)"#,
            )
            .bind(view_id)
            .bind(&file.logical_path)
            .bind(&file.physical_path)
            .bind(file.size_bytes)
            .bind(file.introduced_by)
            .execute(&mut *tx)
            .await?;
        }

        tx.commit().await?;
        Ok(())
    }

