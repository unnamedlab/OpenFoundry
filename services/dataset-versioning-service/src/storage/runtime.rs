    /// Walks `parent_branch_id` from the requested branch up to the
    /// root, returning rows in *child → root* order. Used by the
    /// `GET /branches/{branch}/ancestry` endpoint.
    pub async fn list_branch_ancestry(
        &self,
        branch_id: Uuid,
    ) -> Result<Vec<RuntimeBranch>, sqlx::Error> {
        let sql = format!(
            r#"WITH RECURSIVE chain AS (
                   SELECT {cols}, 0 AS depth
                     FROM dataset_branches
                    WHERE id = $1 AND deleted_at IS NULL
                   UNION ALL
                   SELECT {qual_cols}, c.depth + 1
                     FROM dataset_branches b
                     JOIN chain c ON b.id = c.parent_branch_id
                    WHERE b.deleted_at IS NULL
               )
               SELECT {cols} FROM chain ORDER BY depth ASC"#,
            cols = BRANCH_COLUMNS,
            qual_cols = BRANCH_COLUMNS
                .split(',')
                .map(|c| format!("b.{}", c.trim()))
                .collect::<Vec<_>>()
                .join(", "),
        );
        sqlx::query_as::<_, RuntimeBranch>(&sql)
            .bind(branch_id)
            .fetch_all(&self.pool)
            .await
    }

