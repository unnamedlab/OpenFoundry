    pub async fn replace_fallbacks(
        &self,
        branch_id: Uuid,
        chain: &[String],
    ) -> Result<(), sqlx::Error> {
        let mut tx = self.pool.begin().await?;

        sqlx::query("DELETE FROM dataset_branch_fallbacks WHERE branch_id = $1")
            .bind(branch_id)
            .execute(&mut *tx)
            .await?;

        for (i, name) in chain.iter().enumerate() {
            sqlx::query(
                r#"INSERT INTO dataset_branch_fallbacks
                       (branch_id, position, fallback_branch_name)
                   VALUES ($1, $2, $3)"#,
            )
            .bind(branch_id)
            .bind(i as i32)
            .bind(name.trim())
            .execute(&mut *tx)
            .await?;
        }

        // Mirror the chain into the denormalised array on
        // `dataset_branches.fallback_chain` so the proto/Branch payload
        // is a single SELECT.
        let normalised: Vec<String> = chain
            .iter()
            .map(|s| s.trim().to_string())
            .filter(|s| !s.is_empty())
            .collect();
        sqlx::query(
            r#"UPDATE dataset_branches
                  SET fallback_chain = $2,
                      updated_at = NOW()
                WHERE id = $1"#,
        )
        .bind(branch_id)
        .bind(&normalised)
        .execute(&mut *tx)
        .await?;

        tx.commit().await?;
        Ok(())
    }

